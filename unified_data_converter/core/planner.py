@dataclass
class Job:
    id: str
    target_path: str
    processor_name: str
    params: Dict[str, Any]
    context: Dict[str, Any]
    dependencies: List[str] = field(default_factory=list)
    
    # Hash for resume capability
    # We will compute this later or now. 
