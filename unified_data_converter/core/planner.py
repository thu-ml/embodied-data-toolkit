class Planner:
    def __init__(self, resolver: ParamResolver):
        self.resolver = resolver

    def plan(self, target_structure: List[Dict], context_tree: ContextNode) -> List[Job]:
        """
        Generates a list of jobs based on the target structure and context tree.
        """
        all_jobs = []
        # We start with the root context node (dataset level)
        # But target_structure is a list of roots.
        
        # Initial dependencies: None
        last_layer_jobs = [] 
        
        for config_node in target_structure:
            new_jobs = self._process_node(config_node, context_tree, last_layer_jobs)
            all_jobs.extend(new_jobs)
            # For the top level, sequential processing of structure nodes?
            # Usually yes.
            last_layer_jobs = [j.id for j in new_jobs]

        return all_jobs

    def _process_node(self, 
                      config_node: Dict, 
                      context_node: ContextNode, 
                      dependency_ids: List[str]) -> List[Job]:
        
        node_type = config_node.get("type", "file")
        iterator_name = config_node.get("iterator")
        
        jobs = []

        # 1. Determine Context(s) to operate on
        target_contexts = []
        if iterator_name:
            
            if context_node.iterator_name == iterator_name:
                 # We are already here (e.g. recursive call). 
                 # But usually _process_node is called from parent.
                 target_contexts = [context_node]
            else:
                 # Find direct children matching iterator?
                 # ContextNode.find_descendants finds all. 
                 # But we usually only want the *immediate* next level of iterator 
                 # relative to where we are in the structure.
                 # Simplified: Just find direct children that are this iterator.
                 target_contexts = [child for child in context_node.children if child.iterator_name == iterator_name]
        else:
            # Single execution context (Current Node)
            target_contexts = [context_node]

        # 2. Process for each context
        for ctx in target_contexts:
            current_jobs = self._create_jobs_for_context(config_node, ctx, dependency_ids)
            jobs.extend(current_jobs)
            
        return jobs

