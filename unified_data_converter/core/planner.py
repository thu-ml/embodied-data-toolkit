    def _recurse_structure(self, 
                           structure_list: List[Dict], 
                           context_node: ContextNode, 
                           parent_target_path: str,
                           dependencies: List[str]) -> List[Job]:
        
        all_jobs_in_sequence = []
        
        # "previous_siblings" dependencies
        # Initially, the first child depends on `dependencies` passed in (parent's dependencies)
        current_dependencies = list(dependencies)
        
        for config in structure_list:
            node_jobs = []
            
            iterator_name = config.get("iterator")
            
            # Determine contexts
            contexts_to_process = []
            if iterator_name:
                # Find children of current context_node that match
                # If context_node IS the parent (e.g. Root), children are Tasks.
                contexts_to_process = [c for c in context_node.children if c.iterator_name == iterator_name]
                # If none found? e.g. empty task list.
            else:
                contexts_to_process = [context_node]
            
            # Collect all jobs generated by this config across all contexts
            # These will become dependencies for the NEXT config in structure_list
            this_step_all_job_ids = []

            for ctx in contexts_to_process:
                # Resolve Name
                raw_name = config.get("name", "")
                resolved_name = self.resolver._resolve_string(raw_name, ctx.get_full_context())
                
                # Full Target Path
                # If parent_target_path is dest_root, and resolved_name is "task_1", path is "root/task_1"
                current_target_path = os.path.join(parent_target_path, resolved_name)
                
                # 1. Create Job for this node (if it has a processor)
                processor_name = config.get("processor")
                
                my_job_id = None
                if processor_name:
                    
                    params = config.get("params", {})
                    resolved_params = self.resolver.resolve(params, ctx.get_full_context(), ctx)
                    
                    job = Job(
                        id=str(uuid.uuid4()),
                        target_path=current_target_path,
                        processor_name=processor_name,
                        params=resolved_params,
                        context=ctx.get_full_context(),
                        dependencies=list(current_dependencies) # Copy
                    )
                    all_jobs_in_sequence.append(job)
                    node_jobs.append(job)
                    my_job_id = job.id
                    this_step_all_job_ids.append(my_job_id)

                # 2. Recurse children
                children_config = config.get("children", [])
                if children_config:
                    # The children depend on:
                    # - The job of the current node (if it exists)
                    # - The dependencies of the current node (if current node has no job, it's a pass-through folder)
                    
                    child_deps = list(current_dependencies)
                    if my_job_id:
                        child_deps.append(my_job_id)
                    
                    child_jobs = self._recurse_structure(
                        children_config, 
                        ctx, 
                        current_target_path, 
                        child_deps
                    )
                    
                    all_jobs_in_sequence.extend(child_jobs)
                    node_jobs.extend(child_jobs)
                    
                    # If this node was just a folder (no processor), 
                    # effectively the "output" of this node is the output of its children.
                    # But for "Implicit Order", the NEXT sibling in `structure_list`
                    # should wait for THIS node and ALL its children to finish.
                    
                    # Collect all IDs from children to add to dependencies of next sibling
                    this_step_all_job_ids.extend([j.id for j in child_jobs])

            # Update dependencies for the next sibling in the list
            # The next sibling must wait for ALL instances of this current config (all iterations)
            current_dependencies = this_step_all_job_ids
            
        return all_jobs_in_sequence

